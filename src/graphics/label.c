static void label_free(boxing_component * label)
{
    boxing_component_free(label);
    boxing_memory_free(SMEMBER(label));
}

static void render(boxing_component * label, boxing_painter * painter)
{
    const int width  = label->size.x;
    const int height = label->size.y;
    int line = 0;
    char font_bitmap[5*7];

    const int max_position = ((SMEMBER(alignment) == BOXING_ALIGN_HORIZONTAL) ? width : height) - 6;
    int pos = 0;
    size_t label_length = boxing_string_length(SMEMBER(label));
    int foreground_color = boxing_component_get_foreground_color(label);
    int background_color = boxing_component_get_background_color(label);
    for(char * it = SMEMBER(label); it != SMEMBER(label) + label_length; it++, pos += 6)
    {
        if(!(pos < max_position))
        {
            pos = 0;
            line++;
        }
        if(((unsigned char)*it < 0x20) || ((unsigned char)*it > 0x7F))
        {
            continue;
        }

        unsigned char * font = Font5x7 + (*it - 0x20)*5;
        for(int i = 0; i < 5; i++, font++)
        {
            for(int j = 0; j < 7; j++)
            {
                if(SMEMBER(alignment) == BOXING_ALIGN_HORIZONTAL)
                    font_bitmap[(6-j)*5+i] =  ((*font >> (6-j)) & 1) ? foreground_color : background_color;
                else
                    font_bitmap[(j) + 7*i] =  ((*font >> (6-j)) & 1) ? foreground_color : background_color;
            }
        }

        if(SMEMBER(alignment) == BOXING_ALIGN_HORIZONTAL)
            painter->draw_image(painter, pos, line*8, font_bitmap, 5, 0, 0, 5, 7);
        else
            painter->draw_image(painter, width-7-line*8, pos, font_bitmap, 7, 0, 0, 7, 5);

    }
    boxing_component_render(label, painter);
}
