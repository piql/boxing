//----------------------------------------------------------------------------
/*!
 *  \brief Initializes all structure pointers and variables.
 *
 *  Initializes all input structure pointers and variables according to the input data.
 *
 *  \param[in] label      Pointer to the boxing_label structure.
 *  \param[in] text       Label taxt.
 *  \param[in] alignment  Label text alignment.
 */

void boxing_label_init(boxing_label * label, const char * text, enum boxing_align alignment)
{
    boxing_component_init((boxing_component*)label, NULL);

    label->base.render = render;
    label->base.free = label_free;
    label->label = boxing_string_clone(text);
    label->alignment = alignment;
}


//----------------------------------------------------------------------------
/*!
 *  \brief Set new label text.
 *
 *  Set new label text in the input boxing_label structure.
 *
 *  \param[in] label  Pointer to the boxing_label structure.
 *  \param[in] text   New label text.
 */

void boxing_label_set_label(boxing_label * label, const char * text)
{
    boxing_memory_free(label->label);
    label->label = boxing_string_clone(text);
}


